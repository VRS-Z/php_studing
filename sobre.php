    <H1>Um pouco sobre HOLLOW KNIGHT</H1>
    <P>Origem do Jogo Indie "Hollow Knight"

        Hollow Knight é um jogo indie de ação e aventura no estilo Metroidvania, desenvolvido pelo estúdio australiano Team Cherry.<br> <br> O jogo foi lançado em 2017, mas sua história começou alguns anos antes, em 2014, quando três amigos decidiram unir forças para criar um projeto próprio após trabalhar em outras áreas da indústria de jogos.<br> <br>
        
        Os fundadores do Team Cherry, Ari Gibson, William Pellen e Jack Vine, tiveram a ideia de Hollow Knight enquanto estavam envolvidos no desenvolvimento de outros jogos menores. <br> <br> Ari Gibson, o diretor artístico e co-fundador, foi responsável pela arte e estilo visual do jogo, enquanto William Pellen, que assumiu o papel de designer e programador, cuidou da mecânica do jogo. Jack Vine, por sua vez, trabalhou principalmente em efeitos sonoros e música. <br> <br> Eles já tinham uma paixão por jogos de exploração e ação, e Hollow Knight nasceu desse amor por mundos interconectados e desafiadores. <br> <br>
        
        A inspiração para o jogo veio de títulos como Metroid e Castlevania, além de influências de outras produções mais recentes como Dark Souls, que também incorporam exploração desafiadora e elementos de combate exigentes. <br> <br> O mundo sombrio e encantador de Hollow Knight, com seu visual desenhado à mão e atmosfera imersiva, foi uma escolha deliberada dos criadores para criar uma experiência única e misteriosa.</P>
